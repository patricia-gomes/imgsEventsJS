/*
As 3 funções selecionam o id da imagem principal e subistitui a imagem de
acordo com a função chamada.O que muda nas 3 funções são o caminho das 
imagens.
*/
function imgCat() {
	document.getElementById('imgPrincipal').src= "assets/imgs/cat.jpg";
}
function imgLion() {
	document.getElementById('imgPrincipal').src = "assets/imgs/lion.jpg";
}
function imgDogs() {
	document.getElementById('imgPrincipal').src = "assets/imgs/dogs.jpg";
}